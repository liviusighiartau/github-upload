"""
Examples to show how strings work in Python

Sequence of characters
Contains a-z, 0-9, any special characters
Should be defined in double quotes or single quotes
"""

a = "This is a simple string"
b = 'Using single quotes'

print(a)
print(b)

c = "Need to you some 'quotes' inside this string"

print(c)

d = "Another way to handle \"quotes\""  # \ is used when you want to ignore a functionality

print(d)

a = "This is a single \
    string"

print(a)